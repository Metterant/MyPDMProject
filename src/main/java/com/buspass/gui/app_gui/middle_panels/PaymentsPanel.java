    private void deletePaymentButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_deletePaymentButtonActionPerformed
        String input = javax.swing.JOptionPane.showInputDialog(this, "Enter PaymentID to delete:", "Delete Payment",
                javax.swing.JOptionPane.QUESTION_MESSAGE);
        if (input == null)
            return;
        input = input.trim();
        if (input.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "PaymentID required.", "Input",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        int paymentId;
        try {
            paymentId = Integer.parseInt(input);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid PaymentID.", "Input error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Delete PaymentID " + paymentId + "?", "Confirm",
                javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm != javax.swing.JOptionPane.YES_OPTION)
            return;
        try {
            boolean ok = paymentQuery.deletePayment(paymentId);
            if (ok) {
                javax.swing.JOptionPane.showMessageDialog(this, "Payment deleted.", "Success",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
                resultTable.setModel(new javax.swing.table.DefaultTableModel());
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Payment not found or failed to delete.", "Failure",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error deleting payment: " + ex.getMessage(), "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }// GEN-LAST:event_deletePaymentButtonActionPerformed

