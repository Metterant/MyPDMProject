    private void deleteRouteButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_deleteRouteButtonActionPerformed
        String input = javax.swing.JOptionPane.showInputDialog(this, "Enter RouteID to delete:", "Delete Route",
                javax.swing.JOptionPane.QUESTION_MESSAGE);
        if (input == null)
            return;
        input = input.trim();
        if (input.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "RouteID required.", "Input",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        int routeId;
        try {
            routeId = Integer.parseInt(input);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid RouteID.", "Input error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Delete RouteID " + routeId + "?", "Confirm",
                javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm != javax.swing.JOptionPane.YES_OPTION)
            return;
        try {
            boolean ok = routeQuery.removeRoute(routeId);
            if (ok) {
                javax.swing.JOptionPane.showMessageDialog(this, "Route deleted.", "Success",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
                middlePanel.setTableContents(resultTable, routeQuery.getAllRoutes());
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Route not found or failed to delete.", "Failure",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }// GEN-LAST:event_deleteRouteButtonActionPerformed

