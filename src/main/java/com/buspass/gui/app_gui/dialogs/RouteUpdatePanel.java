    private int parseInt(String s, int fallback) { 
        try { 
            return Integer.parseInt(s); 
        } catch (Exception e) { 
            return fallback; 
        } 
    }

    private Integer parseIntNullable(String s) { 
        if (s==null||s.isEmpty()) return null; 
        try { 
            return Integer.parseInt(s); 
        } catch (Exception e) { 
            return null; 
        } 
    }

    private void setRouteFields(Map<String,Object> route) {
        routeNameField.setText(asString(route.get("RouteName")));
        startField.setText(asString(route.get("StartLocation")));
        endField.setText(asString(route.get("EndLocation")));
        fareField.setText(asString(route.get(""), asString(route.get("Fare"))));
        distanceField.setText(asString(route.get("Distance")));
        durationField.setText(asString(route.get("Duration")));        
    }
    
    private String asString(Object o) { return o==null?"":o.toString(); }
    private String asString(Object primary, String alt) { return primary!=null?primary.toString(): (alt==null?"":alt); }

    private void fetchRoute() {
        int routeId = getRouteId();
        if (routeId <= 0) { 
            JOptionPane.showMessageDialog(this, "Enter a valid RouteID.", "Input", JOptionPane.WARNING_MESSAGE); 
            return; 
        }
        currentRoute = routeQuery.getRouteById(routeId);

        if (currentRoute == null) { 
            JOptionPane.showMessageDialog(this, "Route not found.", "Not Found", JOptionPane.INFORMATION_MESSAGE); 
            return; 
        }
        setRouteFields(currentRoute);
    }

    private void performUpdate() {
        int routeId = getRouteId();
        if (routeId <= 0) { JOptionPane.showMessageDialog(this, "Provide RouteID first.", "Validation", JOptionPane.WARNING_MESSAGE); return; }
        if (currentRoute == null) currentRoute = routeQuery.getRouteById(routeId);
        if (currentRoute == null) { JOptionPane.showMessageDialog(this, "Fetch route before updating.", "Validation", JOptionPane.WARNING_MESSAGE); return; }

        String newRouteName = getRouteName();
        String newStart = getStart();
        String newEnd = getEnd();
        Double newFare = getFare();
        Float newDistance = getDistance();
        Integer newDuration = getDuration();

        StringBuilder summary = new StringBuilder();
        boolean anyChange = false;
        try {
            if (!newRouteName.isEmpty() && !newRouteName.equals(asString(currentRoute.get("RouteName")))) {
                if (routeQuery.updateRouteName(routeId, newRouteName)) { summary.append("Route Name updated.\n"); anyChange = true; }
            }
            if (newStart != null && !asString(currentRoute.get("StartLocation")).equals(String.valueOf(newStart))) {
                if (routeQuery.updateStartLocation(routeId, newStart)) { summary.append("Start Location updated.\n"); anyChange = true; }
            }
            if (newEnd != null && !asString(currentRoute.get("EndLocation")).equals(String.valueOf(newEnd))) {
                if (routeQuery.updateEndLocation(routeId, newEnd)) { summary.append("End Location updated.\n"); anyChange = true; }
            }
            if (newFare != null && !asString(currentRoute.get("Fare")).equals(String.valueOf(newFare))) {
                if (routeQuery.updateFare(routeId, newFare)) { summary.append("Fare updated.\n"); anyChange = true; }
            }
            if (newDistance != null && !asString(currentRoute.get("Distance")).equals(String.valueOf(newDistance))) {
                if (routeQuery.updateDistance(routeId, newDistance)) { summary.append("Distance updated.\n"); anyChange = true; }
            }
            if (newDuration != null && !asString(currentRoute.get("Duration")).equals(String.valueOf(newDuration))) {
                if (routeQuery.updateDuration(routeId, newDuration)) { summary.append("Duration updated.\n"); anyChange = true; }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Update failed: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (anyChange) {
            JOptionPane.showMessageDialog(this, summary.toString(), "Update Success", JOptionPane.INFORMATION_MESSAGE);
            currentRoute = routeQuery.getRouteById(routeId); // refresh cache
            setRouteFields(currentRoute);
        } else {
            JOptionPane.showMessageDialog(this, "No changes applied.", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }
